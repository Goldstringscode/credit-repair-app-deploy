import { type NextRequest, NextResponse } from "next/server"
import OpenAI from "openai"

export async function POST(request: NextRequest) {
  try {
    const body = await request.json()
    const {
      originalExplanation,
      disputeReason,
      creditorName,
      accountNumber,
      desiredOutcome,
      supportingDocs,
      previousDisputes
    } = body

    // Validate required fields
    if (!originalExplanation || !disputeReason || !creditorName) {
      return NextResponse.json(
        {
          success: false,
          error: "Missing required fields",
          details: "originalExplanation, disputeReason, and creditorName are required"
        },
        { status: 400 }
      )
    }

    // Check if OpenAI is available
    const openaiApiKey = process.env.OPENAI_API_KEY
    if (!openaiApiKey) {
      return NextResponse.json(
        {
          success: false,
          error: "OpenAI not available",
          details: "OPENAI_API_KEY environment variable is not set"
        },
        { status: 500 }
      )
    }

    const openai = new OpenAI({
      apiKey: openaiApiKey,
    })

    // Create AI prompt for enhancement
    const prompt = `You are an expert credit repair specialist. Enhance the following basic dispute explanation into a detailed, professional, and legally sound explanation that would be effective in a credit dispute letter.

ORIGINAL EXPLANATION:
${originalExplanation}

DISPUTE CONTEXT:
- Dispute Reason: ${disputeReason}
- Creditor: ${creditorName}
- Account Number: ${accountNumber || 'Not provided'}
- Desired Outcome: ${desiredOutcome || 'Remove from credit report'}
- Supporting Documentation: ${supportingDocs ? 'Available' : 'Not available'}
- Previous Disputes: ${previousDisputes ? 'Yes' : 'No'}

ENHANCEMENT REQUIREMENTS:
1. Expand the explanation to be more detailed and professional
2. Include relevant legal references (FCRA, consumer rights)
3. Add specific details about the impact on credit score
4. Include timeline information if relevant
5. Add professional language and tone
6. Make it persuasive and compelling
7. Keep it factual and accurate
8. Maintain the core message from the original explanation

Generate an enhanced explanation that is 2-3 times longer than the original, more professional, and legally sound. Focus on making it compelling for credit bureaus to take action.`

    console.log("🤖 Enhancing explanation with AI...")

    const completion = await openai.chat.completions.create({
      model: "gpt-4",
      messages: [
        {
          role: "system",
          content: "You are an expert credit repair specialist with deep knowledge of FCRA, consumer rights, and effective dispute letter writing. Your task is to enhance basic dispute explanations into professional, detailed, and legally sound explanations."
        },
        {
          role: "user",
          content: prompt
        }
      ],
      temperature: 0.7,
      max_tokens: 1000,
      presence_penalty: 0.3,
      frequency_penalty: 0.5,
    })

    const enhancedExplanation = completion.choices[0]?.message?.content

    if (!enhancedExplanation) {
      throw new Error("No content generated by AI")
    }

    console.log("✅ Explanation enhanced successfully")

    return NextResponse.json({
      success: true,
      data: {
        enhancedExplanation: enhancedExplanation.trim(),
        originalLength: originalExplanation.length,
        enhancedLength: enhancedExplanation.length,
        improvement: Math.round(((enhancedExplanation.length - originalExplanation.length) / originalExplanation.length) * 100)
      },
      message: "Explanation enhanced successfully with AI"
    })

  } catch (error) {
    console.error("❌ AI explanation enhancement failed:", error)
    
    return NextResponse.json(
      {
        success: false,
        error: "Failed to enhance explanation",
        details: error instanceof Error ? error.message : "Unknown error occurred"
      },
      { status: 500 }
    )
  }
}
