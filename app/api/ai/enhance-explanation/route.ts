import { type NextRequest, NextResponse } from 'next/server'
import Anthropic from '@anthropic-ai/sdk'
import { sanitizeAiFields } from '@/lib/sanitize-ai-input'

export const dynamic = 'force-dynamic'

export async function POST(request: NextRequest) {
  try {
    const body = await request.json()
    const {
      originalExplanation,
      disputeReason,
      creditorName,
      accountNumber,
      desiredOutcome,
      supportingDocs,
      previousDisputes,
    } = body

    if (!originalExplanation || !disputeReason || !creditorName) {
      return NextResponse.json(
        { success: false, error: 'Missing required fields', details: 'originalExplanation, disputeReason, and creditorName are required' },
        { status: 400 }
      )
    }

    // Sanitize all user fields before they enter the AI prompt
  let sanitized: Record<string,string>
  try {
    sanitized = sanitizeAiFields({
      originalExplanation: originalExplanation ?? '',
      disputeReason: disputeReason ?? '',
      creditorName: creditorName ?? '',
      accountNumber: accountNumber ?? '',
      desiredOutcome: desiredOutcome ?? '',
    })
  } catch {
    return NextResponse.json({ success: false, error: 'Invalid input detected. Please revise your submission.' }, { status: 400 })
  }

  const apiKey = process.env.ANTHROPIC_API_KEY
    if (!apiKey) {
      return NextResponse.json(
        { success: false, error: 'AI service not configured', details: 'ANTHROPIC_API_KEY is not set' },
        { status: 503 }
      )
    }

    const prompt = `You are an expert credit repair specialist with deep knowledge of FCRA, consumer rights, and effective dispute letter writing. Enhance the following basic dispute explanation into a detailed, professional, and legally sound explanation.

ORIGINAL EXPLANATION:
${sanitized.originalExplanation}

DISPUTE CONTEXT:
- Dispute Reason: ${sanitized.disputeReason}
- Creditor: ${sanitized.creditorName}
- Account Number: ${sanitized.accountNumber || 'Not provided'}
- Desired Outcome: ${sanitized.desiredOutcome || 'Remove from credit report'}
- Supporting Documentation: ${supportingDocs ? 'Available' : 'Not available'}
- Previous Disputes: ${previousDisputes ? 'Yes' : 'No'}

INSTRUCTIONS:
1. Expand the explanation with specific FCRA violations or inaccuracies
2. Include relevant legal references (FCRA Section 611, 623, etc.) where appropriate
3. Make it professional, assertive, and factually grounded
4. Keep it concise but comprehensive (150-250 words)
5. Focus on why this item should be removed or corrected
6. Do NOT include salutations, closings, or letter formatting — just the explanation paragraph(s)

Provide ONLY the enhanced explanation text, nothing else.`

    const anthropic = new Anthropic({ apiKey })
    const message = await anthropic.messages.create({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 1024,
      system: 'You are an expert credit repair specialist with deep knowledge of FCRA, consumer rights, and effective dispute letter writing. Provide only the requested content with no additional commentary.',
      messages: [{ role: 'user', content: prompt }],
    })

    const enhancedExplanation = message.content[0]?.type === 'text' ? message.content[0].text.trim() : null

    if (!enhancedExplanation) {
      throw new Error('No content generated by AI')
    }

    console.log('✅ Explanation enhanced via Claude Haiku')

    return NextResponse.json({
      success: true,
      data: {
        enhancedExplanation,
        originalExplanation,
        model: 'claude-haiku-4-5-20251001',
        improvementSuggestions: [
          'Added specific FCRA violation references',
          'Strengthened legal language',
          'Clarified the requested resolution',
        ],
      },
    })
  } catch (error: any) {
    console.error('Enhance explanation error:', error)
    return NextResponse.json(
      { success: false, error: error.message || 'Failed to enhance explanation' },
      { status: 500 }
    )
  }
}
