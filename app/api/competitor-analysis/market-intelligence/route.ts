import { type NextRequest, NextResponse } from "next/server"
import { marketIntelligence, calculateMarketOpportunity, competitors } from "@/lib/competitor-analysis"

export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url)
    const affiliateId = searchParams.get("affiliateId")
    const includeForecasts = searchParams.get("forecasts") === "true"

    if (!affiliateId) {
      return NextResponse.json({ error: "Affiliate ID is required" }, { status: 400 })
    }

    // Mock affiliate performance for market opportunity calculation
    const affiliatePerformance = {
      marketShare: 3.2,
      monthlyRevenue: 125000,
      customerBase: 1500,
      growthRate: 15.2,
    }

    const marketOpportunity = calculateMarketOpportunity(affiliatePerformance, competitors)

    const response: any = {
      marketIntelligence,
      marketOpportunity,
      competitiveLandscape: {
        marketLeader: competitors.find((c) => c.marketShare === Math.max(...competitors.map((c) => c.marketShare))),
        emergingPlayers: competitors.filter((c) => c.marketShare < 5 && c.performance.brandStrength > 70),
        consolidationRisk: "Medium",
        newEntrantThreat: "High",
      },
    }

    if (includeForecasts) {
      // In a real app, these would be generated by ML models
      response.forecasts = {
        marketSizeProjection: {
          "2024": 4.2,
          "2025": 4.6,
          "2026": 5.0,
          "2027": 5.4,
          "2028": 5.9,
        },
        competitorMovement: [
          {
            competitor: "Credit Karma",
            prediction: "Likely to expand into direct credit repair services",
            probability: 0.75,
            impact: "High",
          },
          {
            competitor: "Lexington Law",
            prediction: "May acquire smaller competitors",
            probability: 0.6,
            impact: "Medium",
          },
        ],
        regulatoryChanges: [
          {
            change: "Enhanced fee disclosure requirements",
            timeline: "Q2 2024",
            impact: "Medium",
            preparationNeeded: "Update marketing materials and contracts",
          },
          {
            change: "New state licensing requirements",
            timeline: "Q4 2024",
            impact: "High",
            preparationNeeded: "Obtain licenses in 5 additional states",
          },
        ],
      }
    }

    return NextResponse.json({
      success: true,
      data: response,
    })
  } catch (error) {
    console.error("Market intelligence error:", error)
    return NextResponse.json({ error: "Failed to fetch market intelligence" }, { status: 500 })
  }
}
