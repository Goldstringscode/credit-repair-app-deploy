import { Payment, User } from './types'

export interface PDFGenerator {
  generatePaymentHistory(payments: Payment[], user: User): Promise<Buffer>
}

export class MockPDFGenerator implements PDFGenerator {
  async generatePaymentHistory(payments: Payment[], user: User): Promise<Buffer> {
    // Create a simple HTML-based PDF content
    const htmlContent = `
<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <title>Payment History - ${user.email}</title>
    <style>
        body { font-family: Arial, sans-serif; margin: 20px; }
        .header { text-align: center; margin-bottom: 30px; }
        .header h1 { color: #333; margin-bottom: 10px; }
        .header p { color: #666; }
        .summary { background: #f5f5f5; padding: 15px; border-radius: 5px; margin-bottom: 20px; }
        .summary h3 { margin-top: 0; }
        .summary-grid { display: grid; grid-template-columns: 1fr 1fr 1fr; gap: 20px; }
        .summary-item { text-align: center; }
        .summary-value { font-size: 24px; font-weight: bold; color: #2563eb; }
        .summary-label { font-size: 14px; color: #666; margin-top: 5px; }
        table { width: 100%; border-collapse: collapse; margin-top: 20px; }
        th, td { padding: 12px; text-align: left; border-bottom: 1px solid #ddd; }
        th { background-color: #f8f9fa; font-weight: 600; }
        .status { padding: 4px 8px; border-radius: 4px; font-size: 12px; font-weight: 500; }
        .status-succeeded { background-color: #d1fae5; color: #065f46; }
        .status-pending { background-color: #fef3c7; color: #92400e; }
        .status-failed { background-color: #fee2e2; color: #991b1b; }
        .status-refunded { background-color: #e5e7eb; color: #374151; }
        .amount { font-family: monospace; font-weight: 600; }
        .footer { margin-top: 40px; text-align: center; color: #666; font-size: 12px; }
    </style>
</head>
<body>
    <div class="header">
        <h1>Payment History Report</h1>
        <p>Generated on ${new Date().toLocaleDateString()} at ${new Date().toLocaleTimeString()}</p>
        <p>User: ${user.email}</p>
    </div>

    <div class="summary">
        <h3>Summary</h3>
        <div class="summary-grid">
            <div class="summary-item">
                <div class="summary-value">${payments.length}</div>
                <div class="summary-label">Total Transactions</div>
            </div>
            <div class="summary-item">
                <div class="summary-value">$${(payments.reduce((sum, p) => sum + p.amount, 0) / 100).toFixed(2)}</div>
                <div class="summary-label">Total Amount</div>
            </div>
            <div class="summary-item">
                <div class="summary-value">$${(payments.reduce((sum, p) => sum + p.amount, 0) / 100 / payments.length).toFixed(2)}</div>
                <div class="summary-label">Average Amount</div>
            </div>
        </div>
    </div>

    <table>
        <thead>
            <tr>
                <th>Date</th>
                <th>Description</th>
                <th>Type</th>
                <th>Method</th>
                <th>Amount</th>
                <th>Status</th>
                <th>Transaction ID</th>
            </tr>
        </thead>
        <tbody>
            ${payments.map(payment => `
                <tr>
                    <td>${new Date(payment.createdAt).toLocaleDateString()}</td>
                    <td>${payment.description}</td>
                    <td>${payment.type.replace('_', ' ').toUpperCase()}</td>
                    <td>${payment.method.toUpperCase()}</td>
                    <td class="amount">$${(payment.amount / 100).toFixed(2)}</td>
                    <td><span class="status status-${payment.status}">${payment.status.toUpperCase()}</span></td>
                    <td style="font-family: monospace; font-size: 12px;">${payment.transactionId}</td>
                </tr>
            `).join('')}
        </tbody>
    </table>

    <div class="footer">
        <p>This report was generated by Credit Repair App Billing System</p>
        <p>For support, contact: support@creditrepairapp.com</p>
    </div>
</body>
</html>
    `.trim()

    // Convert HTML to Buffer (in production, use Puppeteer or similar)
    return Buffer.from(htmlContent, 'utf-8')
  }
}

// Export singleton instance
export const pdfGenerator = new MockPDFGenerator()
