#!/usr/bin/env node

/**
 * Email Setup Script for Credit Repair App
 * This script helps users configure their email settings
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

console.log('📧 Credit Repair App - Email Setup\n');

const questions = [
  {
    key: 'EMAIL_SERVICE',
    question: 'Which email service would you like to use?\n1. Gmail (Recommended for development)\n2. SendGrid (Recommended for production)\n3. Mailgun\n4. Amazon SES\nEnter choice (1-4): ',
    validate: (answer) => ['1', '2', '3', '4'].includes(answer),
    error: 'Please enter a number between 1 and 4'
  }
];

const gmailQuestions = [
  {
    key: 'SMTP_USER',
    question: 'Enter your Gmail address: ',
    validate: (answer) => answer.includes('@gmail.com'),
    error: 'Please enter a valid Gmail address'
  },
  {
    key: 'SMTP_PASS',
    question: 'Enter your Gmail App Password (16 characters): ',
    validate: (answer) => answer.length === 16,
    error: 'App Password must be exactly 16 characters'
  }
];

const sendgridQuestions = [
  {
    key: 'SENDGRID_API_KEY',
    question: 'Enter your SendGrid API Key: ',
    validate: (answer) => answer.length > 20,
    error: 'API Key seems too short'
  }
];

const mailgunQuestions = [
  {
    key: 'MAILGUN_API_KEY',
    question: 'Enter your Mailgun API Key: ',
    validate: (answer) => answer.length > 20,
    error: 'API Key seems too short'
  },
  {
    key: 'MAILGUN_DOMAIN',
    question: 'Enter your Mailgun domain: ',
    validate: (answer) => answer.length > 0,
    error: 'Domain cannot be empty'
  }
];

const sesQuestions = [
  {
    key: 'AWS_SES_ACCESS_KEY_ID',
    question: 'Enter your AWS Access Key ID: ',
    validate: (answer) => answer.length > 10,
    error: 'Access Key ID seems too short'
  },
  {
    key: 'AWS_SES_SECRET_ACCESS_KEY',
    question: 'Enter your AWS Secret Access Key: ',
    validate: (answer) => answer.length > 20,
    error: 'Secret Access Key seems too short'
  },
  {
    key: 'AWS_SES_REGION',
    question: 'Enter your AWS region (e.g., us-east-1): ',
    validate: (answer) => answer.length > 0,
    error: 'Region cannot be empty'
  }
];

const commonQuestions = [
  {
    key: 'FROM_EMAIL',
    question: 'Enter your from email address: ',
    validate: (answer) => answer.includes('@'),
    error: 'Please enter a valid email address'
  },
  {
    key: 'FROM_NAME',
    question: 'Enter your from name (e.g., Credit Repair App): ',
    validate: (answer) => answer.length > 0,
    error: 'Name cannot be empty'
  }
];

async function askQuestion(question) {
  return new Promise((resolve) => {
    rl.question(question.question, (answer) => {
      if (question.validate(answer)) {
        resolve(answer);
      } else {
        console.log(`❌ ${question.error}\n`);
        askQuestion(question).then(resolve);
      }
    });
  });
}

async function main() {
  try {
    const config = {};
    
    // Ask for email service
    const serviceChoice = await askQuestion(questions[0]);
    config.EMAIL_SERVICE = serviceChoice;
    
    let serviceQuestions = [];
    switch (serviceChoice) {
      case '1': // Gmail
        serviceQuestions = gmailQuestions;
        config.SMTP_HOST = 'smtp.gmail.com';
        config.SMTP_PORT = '587';
        config.SMTP_SECURE = 'false';
        break;
      case '2': // SendGrid
        serviceQuestions = sendgridQuestions;
        break;
      case '3': // Mailgun
        serviceQuestions = mailgunQuestions;
        break;
      case '4': // Amazon SES
        serviceQuestions = sesQuestions;
        break;
    }
    
    // Ask service-specific questions
    for (const question of serviceQuestions) {
      const answer = await askQuestion(question);
      config[question.key] = answer;
    }
    
    // Ask common questions
    for (const question of commonQuestions) {
      const answer = await askQuestion(question);
      config[question.key] = answer;
    }
    
    // Generate .env.local content
    let envContent = '# Email Configuration\n';
    envContent += '# Generated by setup-email.js\n\n';
    
    if (serviceChoice === '1') { // Gmail
      envContent += '# Gmail SMTP Configuration\n';
      envContent += `SMTP_HOST=${config.SMTP_HOST}\n`;
      envContent += `SMTP_PORT=${config.SMTP_PORT}\n`;
      envContent += `SMTP_SECURE=${config.SMTP_SECURE}\n`;
      envContent += `SMTP_USER=${config.SMTP_USER}\n`;
      envContent += `SMTP_PASS=${config.SMTP_PASS}\n\n`;
    } else if (serviceChoice === '2') { // SendGrid
      envContent += '# SendGrid Configuration\n';
      envContent += `SENDGRID_API_KEY=${config.SENDGRID_API_KEY}\n\n`;
    } else if (serviceChoice === '3') { // Mailgun
      envContent += '# Mailgun Configuration\n';
      envContent += `MAILGUN_API_KEY=${config.MAILGUN_API_KEY}\n`;
      envContent += `MAILGUN_DOMAIN=${config.MAILGUN_DOMAIN}\n\n`;
    } else if (serviceChoice === '4') { // Amazon SES
      envContent += '# Amazon SES Configuration\n';
      envContent += `AWS_SES_ACCESS_KEY_ID=${config.AWS_SES_ACCESS_KEY_ID}\n`;
      envContent += `AWS_SES_SECRET_ACCESS_KEY=${config.AWS_SES_SECRET_ACCESS_KEY}\n`;
      envContent += `AWS_SES_REGION=${config.AWS_SES_REGION}\n\n`;
    }
    
    envContent += '# Email Settings\n';
    envContent += `FROM_EMAIL=${config.FROM_EMAIL}\n`;
    envContent += `FROM_NAME=${config.FROM_NAME}\n`;
    envContent += `APP_URL=http://localhost:3000\n\n`;
    
    envContent += '# Database Configuration\n';
    envContent += 'DATABASE_URL=postgresql://username:password@localhost:5432/creditrepairdb\n\n';
    
    envContent += '# Next.js Configuration\n';
    envContent += 'NEXTAUTH_SECRET=your-nextauth-secret-here\n';
    envContent += 'NEXTAUTH_URL=http://localhost:3000\n';
    
    // Write to .env.local
    const envPath = path.join(process.cwd(), '.env.local');
    fs.writeFileSync(envPath, envContent);
    
    console.log('\n✅ Email configuration saved to .env.local');
    console.log('\n📋 Next steps:');
    console.log('1. Restart your development server');
    console.log('2. Test the invite functionality');
    console.log('3. Check the console logs for email status');
    
    if (serviceChoice === '1') {
      console.log('\n📧 Gmail Setup Reminder:');
      console.log('1. Enable 2-Factor Authentication on your Google account');
      console.log('2. Generate an App Password: Google Account > Security > 2-Step Verification > App passwords');
      console.log('3. Use the App Password (not your regular password)');
    }
    
  } catch (error) {
    console.error('❌ Setup failed:', error.message);
  } finally {
    rl.close();
  }
}

main();

